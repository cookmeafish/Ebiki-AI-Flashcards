const DICTS = { en, es, zh, ja }

// Build a t() bound to a language. Falls back: lang → English → key.
export function makeT(lang) {
  const dict = DICTS[lang] || en
  return (key) => (dict[key] != null ? dict[key] : (en[key] != null ? en[key] : key))
}
